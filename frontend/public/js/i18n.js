const STORAGE_KEY = 'bf_ui_lang';

/**
 * Initializes language from localStorage or default
 */
function initLanguage() {
    let lang = 'en';
    try {
        const stored = localStorage.getItem(STORAGE_KEY);
        if (stored === 'en' || stored === 'my') {
            lang = stored;
        }
    } catch (e) { }

    window.currentLang = lang;
    applyTranslations();
    updateLanguageSwitcherUI();
    // Refresh cart and products to reflect currency changes
    if (typeof window.updateCart === 'function') window.updateCart();
    if (typeof window.renderProducts === 'function') window.renderProducts();
    if (typeof window.renderCustomCakeCart === 'function') window.renderCustomCakeCart();
    if (typeof window.updatePreorderPriceSummary === 'function') window.updatePreorderPriceSummary();
}

/**
 * Sets the application language
 * @param {string} lang 'en' or 'my'
 */
function setLanguage(lang) {
    if (lang !== 'en' && lang !== 'my') return;

    window.currentLang = lang;
    try {
        localStorage.setItem(STORAGE_KEY, lang);
    } catch (e) { }

    applyTranslations();
    updateLanguageSwitcherUI();

    // Refresh cart and products to reflect currency/language changes
    if (typeof window.updateCart === 'function') window.updateCart();
    if (typeof window.renderProducts === 'function') window.renderProducts();
    if (typeof window.renderCustomCakeCart === 'function') window.renderCustomCakeCart();
    if (typeof window.updatePreorderPriceSummary === 'function') window.updatePreorderPriceSummary();
    if (typeof window.renderPromotionBanner === 'function') window.renderPromotionBanner();
    if (typeof window.refreshProductSheetUI === 'function') window.refreshProductSheetUI();

    // Trigger Lucide icons after translation if needed
    if (window.lucide && window.lucide.createIcons) {
        window.lucide.createIcons();
    }
}

/**
 * Scans the DOM for data-t attributes and applies translations
 */
function applyTranslations() {
    const lang = window.currentLang || 'en';
    const elements = document.querySelectorAll('[data-t]');

    elements.forEach(el => {
        const key = el.getAttribute('data-t');
        const translation = STRINGS[lang][key];

        if (translation) {
            // Check if it's an input/textarea with placeholder
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translation;
            } else {
                // For regular elements, replace existing text but preserve icons if possible
                // Better approach: only replace text nodes
                const textNode = Array.from(el.childNodes).find(node => node.nodeType === 3 && node.textContent.trim().length > 0);
                if (textNode) {
                    textNode.textContent = translation;
                } else if (el.children.length === 0) {
                    el.textContent = translation;
                } else {
                    // Complex element (like icon + text), usually the text is inside a span or after an icon
                    const span = el.querySelector('span');
                    if (span) {
                        span.textContent = translation;
                    }
                }
            }
        }
    });
}

/**
 * Converts English digits in a string/number to Myanmar numerals
 * @param {string|number} input 
 * @returns {string}
 */
function toMyanmarNumerals(input) {
    if (input === null || input === undefined) return '';
    const str = String(input);
    const map = {
        '0': '၀', '1': '၁', '2': '၂', '3': '၃', '4': '၄',
        '5': '၅', '6': '၆', '7': '၇', '8': '၈', '9': '၉'
    };
    return str.replace(/[0-9]/g, ch => map[ch] || ch);
}

/**
 * Formats a number as currency based on current language
 * @param {number|string} amount 
 * @returns {string}
 */
function formatCurrency(amount) {
    const lang = window.currentLang || 'en';
    const symbol = 'Ks';
    const num = Number(amount) || 0;

    // Add thousands separator and 2 decimal places
    let formatted = num.toLocaleString('en-US', {
        minimumFractionDigits: 2,
        maximumFractionDigits: 2
    });

    if (lang === 'my') {
        const myNum = toMyanmarNumerals(formatted);
        return `${myNum} ${symbol}`;
    }
    return `${formatted} ${symbol}`;
}

/**
 * Updates the visual state of the language switcher
 */
function updateLanguageSwitcherUI() {
    const lang = window.currentLang || 'en';
    const enBtn = document.getElementById('langEn');
    const myBtn = document.getElementById('langMy');

    if (enBtn && myBtn) {
        if (lang === 'en') {
            enBtn.classList.add('active');
            enBtn.classList.add('btn-primary');
            enBtn.classList.add('text-white');
            enBtn.classList.remove('btn-outline-secondary');

            myBtn.classList.remove('active');
            myBtn.classList.remove('btn-primary');
            myBtn.classList.remove('text-white');
            myBtn.classList.add('btn-outline-secondary');
        } else {
            myBtn.classList.add('active');
            myBtn.classList.add('btn-primary');
            myBtn.classList.add('text-white');
            myBtn.classList.remove('btn-outline-secondary');

            enBtn.classList.remove('active');
            enBtn.classList.remove('btn-primary');
            enBtn.classList.remove('text-white');
            enBtn.classList.add('btn-outline-secondary');
        }
    }
}

